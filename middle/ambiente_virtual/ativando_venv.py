# Para ativar o venv
# É necessário acessar a pasta venv, acessar a pasta Scripts(ou bin)
# e acessar o arquivo activate
# Comando: {nome do ambiente(venv)}\Scripts(ou bin)\activate
# venv\Scripts\activate
# venv\bin\activate
# .\venv\Scripts\activate
# .\venv\bin\activate
# Para desativar, comando:
# deactivate